#!/bin/bash

# “set -e” option causes the script to exit immediately if any command exits with a non-zero exit code.
set -e

echo -e "Current working directory: $(pwd)\n"

echo -e "Installing helm chart ... WAIT\n"

kube_resource_already_exists() {
    eval '$@ 2>&1 | tee /dev/tty | grep -q "already exists" >/dev/null'
}

kube_command='kubectl create namespace fibonacci-dev'

if kube_resource_already_exists "$kube_command"; then
    echo -e "Ignoring above error ...\n"
fi

chmod 755 ./scripts/wait_for_kube_resource.sh
./scripts/wait_for_kube_resource.sh --resource-type namespace --resource fibonacci-dev --namespace fibonacci-dev --resource-query .status.phase --resource-expected-status 'Active'

kube_command='kubectl create secret --namespace fibonacci-dev generic postgres-server-secret --from-literal=POSTGRES_USER=backend --from-literal=POSTGRES_PASSWORD=postgres --from-literal=POSTGRES_DB=fib'

if kube_resource_already_exists "$kube_command"; then
    echo -e "Ignoring above error ...\n"
fi

kube_command='kubectl create secret --namespace fibonacci-dev docker-registry docker-local-registry-secret --docker-server=host.docker.internal:4321 --docker-username=registry_user --docker-password=registry_pass --docker-email=registry_user@email.com'

if kube_resource_already_exists "$kube_command"; then
    echo -e "Ignoring above error ...\n"
fi

helm upgrade --install fibonacci-dev helm/fibonacci/ --namespace fibonacci-dev --values=helm/fibonacci/values.yaml --values=helm/fibonacci/values-dev-env.yaml --values=helm/fibonacci/values-local-registry.yaml --wait --wait-for-jobs

echo -e "\n\nSUCCESS: helm chart installed."
